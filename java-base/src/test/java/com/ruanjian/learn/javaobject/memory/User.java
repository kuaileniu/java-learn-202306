package com.ruanjian.learn.javaobject.memory;

public class User {
    private String username;
    private long age;

    public void setAge(long age) {
        this.age = age;
    }

    public long getAge() {
        return age;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getUsername() {
        return username;
    }
}
